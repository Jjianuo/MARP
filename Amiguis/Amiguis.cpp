void resuelveCaso() {
    int N, M;
    cin >> N >> M;
    Grafo amigos(N);
    int v, w;
    for (int i = 0; i < M; ++i) {
        cin >> v >> w;
        amigos.ponArista(v - 1, w - 1);
    }
    MaximaCompConexa mcc(amigos);
    cout << mcc.maximo() << '\n';

}

